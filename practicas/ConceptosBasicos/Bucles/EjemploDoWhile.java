package ConceptosBasicos.Bucles;

public class EjemploDoWhile {

    public static void main(String[] args) {
        
        boolean condicion = true;

        do
        {
            //intrucciones
            System.out.println("HOLA");


            //Actualizar condicion
            condicion = false;

        } while (condicion);

        System.out.println("ADIOS");
    }
    
}
