package ConceptosBasicos.Bucles;

public class EjemploWhile {

    public static void main(String[] args) {
        
        int numero = 5;

        while (numero >= 0)
        {
            //Conjunto de instrucciones
            System.out.println("HOLA");

            //Actualización de la condición
            numero--;
        }
    }
    
}
